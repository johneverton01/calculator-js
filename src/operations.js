class Operations {
    static sum(a, b) {
        return a + b;
    }

    static sub(a, b) {
        return a - b;
    }

    static division(a, b) {
        return a / b;
    }

    static multiplication(a, b) {
        return a * b;
    }

    static multiplication(a, b) {
        return a * b;
    }

    static power(a, b) {
        return Math.pow(a,b);
    }

    static square(a) {
        return Math.sqrt(a);
    }
}

export default Operations;